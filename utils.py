# Calculator
'''Calculator'''

def add(a: int, b: int) -> int:
    '''sums two integers'''
    return a + b


def subtract(a: int, b: int) -> int:
    '''subtracts two integers'''
    return a - b


def multiply(a: int, b: int) -> int:
    '''multiplies two integers'''
    return a * b


def divide(a: int, b: int) -> float:
    '''divides two integers'''
    return a / b
